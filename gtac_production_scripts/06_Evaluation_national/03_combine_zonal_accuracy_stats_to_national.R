# Combine single-year zonal accuracy outputs across year/model runs for visualization.
# Input CSVs are produced by: 03_run_single_year_zonal_accuracy_stats.R

# Inputs
######################################################
years <- c(2020, 2022, 2023)
study_area <- "CONUS"
project_name_suffixes <- c("Production_newXtable", "Production_newXtable", "Production_")

# Where to export combined results, relative to home_dir
output_dir <- "03_Outputs/08_Meta_Analysis"

# Load libraries and shared functions
######################################################
lib_path <- glue::glue("{this.path::this.proj()}/gtac_production_scripts/00_Library/treeMapLib.R")
source(lib_path)

assembly_lib_path <- glue::glue("{this.path::this.proj()}/gtac_production_scripts/00_Library/zonal_accuracy_assembly_lib.R")
source(assembly_lib_path)

# Combine outputs generated by the single-year script
######################################################
output_dir_abs <- glue::glue("{home_dir}/{output_dir}/")

result <- combine_zonal_accuracy_outputs(
  years = years,
  project_name_suffixes = project_name_suffixes,
  study_area = study_area,
  output_dir = output_dir_abs,
  home_dir = home_dir
)

message("Wrote combined multi-year outputs:")
print(result$output_paths)
